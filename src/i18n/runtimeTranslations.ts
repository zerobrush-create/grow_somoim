export const translateRuntimeText = (value: string, lang: Language) => {
  if (lang === "ko") return value;
  const trimmed = value.trim();
  if (!trimmed || !/[가-힣]/.test(trimmed)) return value;

  const exact = common[lang][trimmed] ?? groupForm[lang][trimmed] ?? phrases[lang][trimmed];
  if (exact) return value.replace(trimmed, exact);

  let translated = trimmed
    .replace(/이미지 추가 \(최대 (\d+)MB\)/g, lang === "en" ? "Add image (max $1MB)" : lang === "ja" ? "画像を追加（最大$1MB）" : lang === "zh" ? "添加图片（最大 $1MB）" : "Добавить изображение (до $1MB)")
    .replace(/이미지는 (\d+)MB 이하만 가능해요/g, lang === "en" ? "Images must be $1MB or less" : lang === "ja" ? "画像は$1MB以下にしてください" : lang === "zh" ? "图片大小必须不超过 $1MB" : "Изображение должно быть не больше $1MB")
    .replace(/태그 \(선택, 최대 (\d+)개\)/g, lang === "en" ? "Tags (optional, up to $1)" : lang === "ja" ? "タグ（任意、最大$1個）" : lang === "zh" ? "标签（可选，最多 $1 个）" : "Теги (необязательно, до $1)")
    .replace(/태그는 최대 (\d+)개까지 추가할 수 있어요/g, lang === "en" ? "You can add up to $1 tags" : lang === "ja" ? "タグは最大$1個まで追加できます" : lang === "zh" ? "最多可添加 $1 个标签" : "Можно добавить до $1 тегов")
    .replace(/(\d+)년\s+(\d+)월/g, lang === "en" ? "$2/$1" : lang === "ru" ? "$2.$1" : "$1/$2")
    .replace(/(\d+)월\s+(\d+)일/g, lang === "en" ? "$1/$2" : lang === "ru" ? "$2.$1" : "$1/$2")
    .replace(/(\d+)시간 전/g, lang === "en" ? "$1h ago" : lang === "ja" ? "$1時間前" : lang === "zh" ? "$1小时前" : "$1 ч. назад")
    .replace(/(\d+)분 전/g, lang === "en" ? "$1m ago" : lang === "ja" ? "$1分前" : lang === "zh" ? "$1分钟前" : "$1 мин. назад")
    .replace(/(\d+)일 전/g, lang === "en" ? "$1d ago" : lang === "ja" ? "$1日前" : lang === "zh" ? "$1天前" : "$1 дн. назад")
    .replace(/(\d+)주일 전/g, lang === "en" ? "$1w ago" : lang === "ja" ? "$1週間前" : lang === "zh" ? "$1周前" : "$1 нед. назад")
    .replace(/방금/g, lang === "en" ? "Just now" : lang === "ja" ? "たった今" : lang === "zh" ? "刚刚" : "Только что")
    .replace(/어제/g, lang === "en" ? "Yesterday" : lang === "ja" ? "昨日" : lang === "zh" ? "昨天" : "Вчера");

  for (const [ko, out] of Object.entries(phrases[lang]).sort((a, b) => b[0].length - a[0].length)) {
    if (ko.length < 2) continue;
    translated = translated.replaceAll(ko, out);
  }

  return value.replace(trimmed, translated);
};
